# Найти сумму чисел ряда 1,2,3,...,60 с использованием функции нахождения суммы.
# Использовать локальные переменные.
def calculate_sum():
    # Локальная переменная для суммы
    total = 0
    # Локальная переменная для счётчика
    n = 60
    # Суммируем от 1 до 60
    for i in range(1, n + 1):
        total += i
    return total

# Вызов функции
sum_result = calculate_sum()
print(f"Сумма чисел ряда от 1 до 60: {sum_result}")